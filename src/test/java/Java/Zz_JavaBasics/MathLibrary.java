package Java.Zz_JavaBasics;

public class MathLibrary {

	public static void main(String[] args) {
		
		System.out.println(Math.class);
		System.out.println(Math.E);
		System.out.println(Math.PI);
		System.out.println(Math.abs(-12));
		System.out.println(Math.abs(-18.2));
		System.out.println(Math.acos(2)); 				//Any number between -1 to 1
		System.out.println(Math.cbrt(27));
		System.out.println(Math.decrementExact(111));
		System.out.println(Math.max(21, 34));
		System.out.println(Math.negateExact(-2));		//Convert negative to positive and vice versa
		System.out.println(Math.pow(2, 3));				//Exponent

	}

}
